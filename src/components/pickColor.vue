    // $(this).children('img').addClass('colorElement');  //为每一个颜色图片添加公共类名
    var classNamePre = 'colorElement';
    $('.colorPanel').each(function () {
      $(this).children('.colorP').each(function (indexC) {
        $(this).children('img').each(function (index, domObj) {
          addClassToImg(domObj, classNamePre, indexC);
          // var color=domObj.attr('src').substr(11,5);
          var color=$(this).attr('src').substr(12,6);
          $(this).attr('alt','#'+color);
          // console.log(color);
          domObj.addEventListener('mouseover', function () {
            $(this).next().fadeIn('slow');
            // console.log($(this).attr('src'));
            //   console.log($(this).attr('alt'));
          });
          domObj.addEventListener('mouseout', function () {
            $(this).next().hide();
          });
          // domObj.next().innerHTML=domObj.attr('alt');
        });
        $(this).children('span').each(function () {
          $(this).addClass('spanInit');
          $(this).addClass('spanInit'+indexC);
          $(this).html($(this).prev().attr('alt'));
        })
      });
    });
    $('div.colorP span').each(function(){
      $(this).innerHTML=$(this).prev().attr('alt');
    });
    function addClassToImg(target, className, num) {
      target.classList.add(className);
      target.classList.add(className + num);
    };
  });
